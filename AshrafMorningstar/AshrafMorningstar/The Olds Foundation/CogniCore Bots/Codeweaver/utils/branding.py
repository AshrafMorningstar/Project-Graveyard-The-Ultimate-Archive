"""
Branding Manager Utility
Author: Ashraf Siddiqui
GitHub: https://github.com/AshrafMorningstar

Handles user branding and attribution in generated projects.
"""

from typing import Dict, Any
from datetime import datetime


class BrandingManager:
    """
    Manages user branding and attribution for generated projects.

    Author: Ashraf Siddiqui
    GitHub: https://github.com/AshrafMorningstar
    """

    def __init__(self, config: Dict[str, Any]):
        """Initialize the branding manager."""
        self.config = config
        self.user_name = config['user']['name']
        self.user_github = config['user']['github']
        self.user_email = config['user'].get('email', '')

    def get_code_header(self, language: str, project_name: str) -> str:
        """
        Generate a code header with author attribution.

        Args:
            language: Programming language
            project_name: Name of the project

        Returns:
            Formatted code header string
        """
        year = datetime.now().year

        # Language-specific comment styles
        comment_styles = {
            'python': ('"""', '"""'),
            'javascript': ('/**', ' */'),
            'typescript': ('/**', ' */'),
            'java': ('/**', ' */'),
            'swift': ('/**', ' */'),
            'go': ('/**', ' */'),
            'rust': ('/**', ' */'),
            'cpp': ('/**', ' */'),
            'c': ('/**', ' */'),
            'ruby': ('=begin', '=end'),
            'php': ('/**', ' */'),
            'html': ('<!--', '-->'),
            'css': ('/**', ' */'),
        }

        start, end = comment_styles.get(language.lower(), ('#', '#'))

        if start in ['"""', '=begin']:
            header = f"""{start}
{project_name}

Author: {self.user_name}
GitHub: {self.user_github}
Year: {year}

Generated by Multi-Language Project Generator Bot
{end}
"""
        elif start in ['/**', '<!--']:
            header = f"""{start}
 * {project_name}
 *
 * Author: {self.user_name}
 * GitHub: {self.user_github}
 * Year: {year}
 *
 * Generated by Multi-Language Project Generator Bot
 {end}
"""
        else:
            header = f"""# {project_name}
#
# Author: {self.user_name}
# GitHub: {self.user_github}
# Year: {year}
#
# Generated by Multi-Language Project Generator Bot

"""

        return header

