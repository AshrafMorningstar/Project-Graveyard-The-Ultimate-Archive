def main():
    base_dir = "Generated_Languages"
    if not os.path.exists(base_dir):
        os.makedirs(base_dir)

    languages = get_languages()
    print(f"Found {len(languages)} languages defined.")

    for lang in languages:
        # Create Category Directory
        cat_dir = os.path.join(base_dir, lang.category)
        if not os.path.exists(cat_dir):
            os.makedirs(cat_dir)

        # Create File
        file_name = f"main.{lang.extension}"
        # Handle duplicates (like .m for matlab/objective-c) by prefixing language name if needed?
        # Actually, they are in different categories usually, or we can just name the file after the language.
        # Let's name the file {LanguageName}.{extension} to be safe and clear.
        safe_name = lang.name.replace(" ", "_").replace("(", "").replace(")", "").replace("/", "")
        file_name = f"{safe_name}.{lang.extension}"

        file_path = os.path.join(cat_dir, file_name)

        print(f"Generating {lang.name} in {file_path}...")

        code_content = lang.generate_code(line_count=105) # Aim for >99

        with open(file_path, "w", encoding="utf-8") as f:
            f.write(code_content)

    print("Generation Complete.")

if __name__ == "__main__":
    main()
